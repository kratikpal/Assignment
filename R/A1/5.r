#Write a R program to demonstrate operator

a <- 6
b <- 2
l1 <- T
l2 <- F
print(paste("+ operator = ",a+b))
print(paste("- operator = ",a-b))
print(paste("* operator = ",a*b))
print(paste("/ operator = ",a/b))
print(paste("logical AND = ",l1&&l2))
print(paste("logical Or = ",l1||l2))
print(paste("Negation  = ",!l1))
print(paste("less than  = ",a<b))
print(paste("greater than  = ",a>b))